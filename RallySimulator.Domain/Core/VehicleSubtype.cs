namespace RallySimulator.Domain.Core
{
    /// <summary>
    /// Represents the vehicle subtypes enumeration.
    /// </summary>
    public enum VehicleSubtype
    {
        /// <summary>
        /// The truck vehicle subtype.
        /// </summary>
        Truck = 1,

        /// <summary>
        /// The terrain car vehicle subtype.
        /// </summary>
        TerrainCar = 2,

        /// <summary>
        /// The sports car vehicle subtype.
        /// </summary>
        SportsCar = 3,

        /// <summary>
        /// The cross motorcycle vehicle subtype.
        /// </summary>
        CrossMotorcycle = 4,

        /// <summary>
        /// The sport motorcycle vehicle subtype.
        /// </summary>
        SportMotorcycle = 5
    }
}
